/**
 * Helper to get a display name with both HK and TW variants.
 * Returns HK name only if TW is the same or absent, or "HK / TW" if they differ.
 */
export function getDualZhName(hkName: string, twName?: string): string {
  if (!twName || twName === hkName) return hkName;
  return `${hkName} / ${twName}`;
}

